#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*buf;

	if (!*lst)
		*lst = new;
	else
	{
		buf = ft_lstlast(*lst);
		buf->next = new;
	}
}
