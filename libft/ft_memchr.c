#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t size)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *)s;
	chr = (unsigned char)c;
	i = 0;
	while (i < size)
	{
		if (*str == chr)
			return (str);
		str++;
		i++;
	}
	return (NULL);
}
